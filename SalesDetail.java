package com.model;

/**
 *
 * @author User
 */
public class SalesDetail {
 int id,price,qty,total;;
  String username,address,date,time,dishname;
  

    public SalesDetail() {
    }

    public SalesDetail(int id, int price, int qty, int total, String username, String address, String date, String time, String dishname) {
        this.id = id;
        this.price = price;
        this.qty = qty;
        this.total = total;
        this.username = username;
        this.address = address;
        this.date = date;
        this.time = time;
        this.dishname = dishname;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public int getQty() {
        return qty;
    }

    public void setQty(int qty) {
        this.qty = qty;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getDishname() {
        return dishname;
    }

    public void setDishname(String dishname) {
        this.dishname = dishname;
    }

  
}
